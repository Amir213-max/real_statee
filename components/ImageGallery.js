'use client';

import { useState } from 'react';
import Image from 'next/image';
import GoogleDriveImage from '@/components/GoogleDriveImage';
import { Swiper, SwiperSlide } from 'swiper/react';
import { Navigation, Pagination } from 'swiper/modules';
import { convertGoogleDriveUrl, isGoogleDriveUrl } from '@/lib/utils';
import 'swiper/css';
import 'swiper/css/navigation';
import 'swiper/css/pagination';

export default function ImageGallery({ images }) {
  const [selectedImage, setSelectedImage] = useState(
    convertGoogleDriveUrl(images?.[0]) || '/default.jpg'
  );

  if (!images || images.length === 0) {
    return (
      <div className="relative w-full h-96 bg-gradient-to-br from-gray-200 to-gray-300 rounded-lg flex items-center justify-center">
        <div className="text-center">
          <svg
            className="w-16 h-16 text-gray-400 mx-auto mb-2"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M4 16l4.586-4.586a2 2 0 012.828 0L16 16m-2-2l1.586-1.586a2 2 0 012.828 0L20 14m-6-6h.01M6 20h12a2 2 0 002-2V6a2 2 0 00-2-2H6a2 2 0 00-2 2v12a2 2 0 002 2z"
            />
          </svg>
          <p className="text-gray-500">No images available</p>
        </div>
      </div>
    );
  }

  return (
    <div className="space-y-4">
      <div className="relative w-full h-96 rounded-lg overflow-hidden">
        {isGoogleDriveUrl(selectedImage) ? (
          <GoogleDriveImage
            src={selectedImage}
            alt="Main"
            fill
            className="object-cover"
          />
        ) : (
          <Image
            src={selectedImage}
            alt="Main"
            fill
            className="object-cover"
            unoptimized
          />
        )}
      </div>
      {images.length > 1 && (
        <Swiper
          modules={[Navigation, Pagination]}
          spaceBetween={10}
          slidesPerView={4}
          navigation
          className="h-24"
        >
          {images.map((img, index) => {
            const convertedUrl = convertGoogleDriveUrl(img);
            return (
              <SwiperSlide key={index}>
                <div
                  className={`relative w-full h-full rounded-lg overflow-hidden cursor-pointer border-2 ${
                    selectedImage === convertedUrl ? 'border-blue-600' : 'border-transparent'
                  }`}
                  onClick={() => setSelectedImage(convertedUrl)}
                >
                  {isGoogleDriveUrl(img) ? (
                    <GoogleDriveImage
                      src={img}
                      alt={`Thumbnail ${index + 1}`}
                      fill
                      className="object-cover"
                    />
                  ) : (
                    <Image
                      src={convertedUrl}
                      alt={`Thumbnail ${index + 1}`}
                      fill
                      className="object-cover"
                      unoptimized
                    />
                  )}
                </div>
              </SwiperSlide>
            );
          })}
        </Swiper>
      )}
    </div>
  );
}

